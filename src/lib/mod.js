function isCloser(value) {
    return typeof value === "object" && value != null && "close" in value && typeof value["close"] === "function";
}
function readableStreamFromReader(reader, options = {
}) {
    const { autoClose =true , chunkSize =16640 , strategy ,  } = options;
    return new ReadableStream({
        async pull (controller) {
            const chunk = new Uint8Array(chunkSize);
            try {
                const read = await reader.read(chunk);
                if (read === null) {
                    if (isCloser(reader) && autoClose) {
                        reader.close();
                    }
                    controller.close();
                    return;
                }
                controller.enqueue(chunk.subarray(0, read));
            } catch (e) {
                controller.error(e);
                if (isCloser(reader)) {
                    reader.close();
                }
            }
        },
        cancel () {
            if (isCloser(reader) && autoClose) {
                reader.close();
            }
        }
    }, strategy);
}
async function serve_file_as_ReadableStream(path25) {
    const content_type = get_content_type(path25);
    if (content_type == undefined) {
        return new Response(null, {
            status: Status.InternalServerError
        });
    } else {
        const headers = new Headers();
        headers.set("content-type", content_type);
        const file = await Deno.open(path25);
        const body = readableStreamFromReader(file);
        const response = new Response(body, {
            status: Status.OK,
            headers
        });
        return response;
    }
}
